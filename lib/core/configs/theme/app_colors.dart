import 'package:flutter/material.dart';

/// A utility class that contains the color palette used throughout the app.
///
/// This class provides a set of predefined colors as static constants, ensuring 
/// consistency in design and making it easier to manage colors across different components.
class AppColors {

  // Primary color used for branding elements or highlights (green).
  static const primary = Color(0xff42C83C);

  // Light background color for screens or components in light mode.
  static const lightBackground = Color(0xffF2F2F2);

  // Dark background color for screens or components in dark mode.
  static const darkBackground = Color(0xff0D0C0C);

  // Grey color for neutral elements like borders, inactive buttons, etc.
  static const grey = Color(0xffBEBEBE);

  // Dark grey color for text, icons, or other UI elements on dark backgrounds.
  static const darkGrey = Color(0xff343434);
}
